function detectPlatform(url) {
  if (!url) return null;
  const u = url.toLowerCase();
  if (u.includes('tiktok.com')) return 'tiktok';
  if (u.includes('instagram.com')) return 'instagram';
  if (u.includes('youtube.com') || u.includes('youtu.be')) return 'youtube';
  if (u.includes('facebook.com') || u.includes('fb.watch') || u.includes('fb.com')) return 'facebook';
  if (u.includes('twitter.com') || u.includes('x.com')) return 'twitter';
  if (u.match(/\.(mp4|webm|mov)(\?|#|$)/i)) return 'direct';
  return null;
}

function fmtDuration(sec) {
  if (!sec) return '\u2014';
  const m = Math.floor(sec / 60), s = Math.floor(sec % 60);
  if (m >= 60) return Math.floor(m / 60) + ':' + String(m % 60).padStart(2, '0') + ':' + String(s).padStart(2, '0');
  return m + ':' + String(s).padStart(2, '0');
}

function getYouTubeId(url) {
  const m = url.match(/(?:youtube\.com\/(?:watch\?v=|embed\/|shorts\/)|youtu\.be\/)([a-zA-Z0-9_-]{11})/);
  return m ? m[1] : null;
}

async function handleDownload() {
  const input = document.getElementById('urlInput');
  const select = document.getElementById('platformSelect');
  const url = input.value.trim();
  if (!url) {
    input.style.borderColor = 'var(--g1)';
    input.focus();
    setTimeout(() => input.style.borderColor = '', 800);
    return;
  }

  const platform = select.value !== 'auto' ? select.value : detectPlatform(url);
  lastUrl = url;
  lastPlatform = platform;
  lastVid = '';

  // Loading state
  document.getElementById('resultDuration').textContent = '\u2026';
  document.getElementById('resultSize').textContent = '\u2026';
  document.getElementById('resultSize').style.display = '';
  document.getElementById('resultQuality').style.display = 'none';
  document.getElementById('resultTitle').textContent = 'Fetching info...';
  document.getElementById('resultThumb').innerHTML = '<div style="width:100%;height:100%;display:flex;align-items:center;justify-content:center;font-size:26px">\u23F3</div>';
  document.getElementById('resultPreview').innerHTML = '<div class="spinner"></div>';
  document.getElementById('result').classList.add('show');
  document.getElementById('dlBtnLabel').textContent = 'Please wait...';
  document.getElementById('dlBtn').disabled = true;

  try {
    const r = await fetch('/api/info?url=' + encodeURIComponent(url));
    const data = await r.json();

    if (data.error) throw new Error(data.error);

    // Build display info
    let title = data.title || 'Video';
    let duration = data.durationFormatted || '\u2014';
    let quality = '';
    let size = '';

    if (data.bestSingle) {
      // Show best single-file quality (what they'll actually get)
      quality = data.bestSingle.quality || '';
      size = data.bestSingle.sizeFormatted || '';
    } else if (data.maxQuality) {
      quality = data.maxQuality;
    }

    // Build preview
    let thumbHtml, previewHtml;

    if (platform === 'youtube') {
      const vid = getYouTubeId(url);
      if (vid) {
        lastVid = vid;
        thumbHtml = '<img src="/api/thumb?v=' + vid + '" style="width:100%;height:100%;object-fit:cover" alt="" onerror="this.outerHTML=\'<div style=width:100%;height:100%;display:flex;align-items:center;justify-content:center;font-size:26px>\uD83C\uDFAC</div>\'">';
        previewHtml = '<div style="width:100%;height:100%;position:relative"><iframe src="/api/embed?v=' + vid + '" allow="autoplay; encrypted-media" allowfullscreen style="width:100%;height:100%;border:none;"></iframe></div>';

        if (data.maxQuality && data.maxQuality !== quality) {
          // Higher quality available but needs merge
          quality = data.maxQuality + ' (\u2192 ' + (size || quality || '360p') + ')';
        }
      }
    }

    if (!thumbHtml) {
      const icons = { tiktok: '\uD83C\uDFB5', instagram: '\uD83D\uDCF8', facebook: '\uD83D\uDCD8', twitter: '\uD835\uDD4E' };
      const icon = icons[platform] || '\uD83C\uDFAC';
      thumbHtml = '<div style="width:100%;height:100%;display:flex;align-items:center;justify-content:center;font-size:32px">' + icon + '</div>';
      previewHtml = '<div style="display:flex;flex-direction:column;align-items:center;justify-content:center;height:100%;gap:12px;padding:20px;text-align:center"><span style="font-size:48px">' + icon + '</span><span style="color:var(--text-dim);font-size:13px">Ready to download</span></div>';
    }

    document.getElementById('resultTitle').textContent = title;
    document.getElementById('resultDuration').textContent = duration;
    document.getElementById('resultSize').textContent = size ? '\u2022 ' + size : '\u2014';
    document.getElementById('resultQuality').textContent = quality ? '\u2022 ' + quality : '';
    document.getElementById('resultQuality').style.display = quality ? '' : 'none';
    document.getElementById('resultThumb').innerHTML = thumbHtml || '<div style="width:100%;height:100%;display:flex;align-items:center;justify-content:center;font-size:26px">\uD83C\uDFAC</div>';
    document.getElementById('resultPreview').innerHTML = previewHtml || '<span class="result-placeholder">Preview</span>';
    document.getElementById('dlBtnLabel').textContent = 'Download ' + (quality.replace(/\(.*?\)/, '').trim() || 'Video');
    document.getElementById('dlBtn').disabled = false;

    setTimeout(() => document.getElementById('result').scrollIntoView({ behavior: 'smooth', block: 'nearest' }), 200);
  } catch (e) {
    document.getElementById('resultTitle').textContent = '\u274C Error: ' + (e.message || 'Failed to load info');
    document.getElementById('resultDuration').textContent = '\u2014';
    document.getElementById('resultSize').textContent = '\u2014';
    document.getElementById('resultThumb').innerHTML = '<div style="width:100%;height:100%;display:flex;align-items:center;justify-content:center;font-size:26px">\u274C</div>';
    document.getElementById('resultPreview').innerHTML = '<span style="color:var(--text-dim);font-size:12px">' + (e.message || 'Error') + '</span>';
    document.getElementById('dlBtnLabel').textContent = 'Retry';
    document.getElementById('dlBtn').disabled = false;
  }
}

async function doDownload() {
  const btn = document.getElementById('dlBtn');
  const label = document.getElementById('dlBtnLabel');

  const url = lastUrl || document.getElementById('urlInput').value.trim();
  if (!url) return;

  btn.disabled = true;
  label.textContent = '\u23F3 Starting...';

  const plat = lastPlatform || detectPlatform(url);

  if (plat === 'youtube') {
    if (!lastVid) lastVid = getYouTubeId(url);
    if (!lastVid) return;
    // Fetch /api/yt and check if result is video or error JSON
    try {
      const r = await fetch('/api/yt?v=' + lastVid);
      const ct = r.headers.get('content-type') || '';
      if (ct.startsWith('video/')) {
        // Got a video stream — download via blob
        label.textContent = '\u23F3 Downloading...';
        const blob = await r.blob();
        const a = document.createElement('a');
        a.href = URL.createObjectURL(blob);
        a.download = lastVid + '.mp4';
        a.style.display = 'none';
        document.body.appendChild(a);
        a.click();
        document.body.removeChild(a);
        URL.revokeObjectURL(a.href);
        label.textContent = 'Download started';
      } else {
        // Got an error (JSON)
        const data = await r.json();
        throw new Error(data.error || 'Download failed (502)');
      }
    } catch (e) {
      label.textContent = '\u274C ' + (e.message || 'Error');
      document.getElementById('resultTitle').textContent = '\u274C ' + (e.message || 'Download failed');
    }
    btn.disabled = false;
    return;
  }

  // Non-YouTube: fetch + pipe
  try {
    label.textContent = '\u23F3 Fetching...';
    const r = await fetch('/api/dl?url=' + encodeURIComponent(url));
    const data = await r.json();
    if (!data.url) throw new Error(data.error || 'No video URL found');

    label.textContent = '\u23F3 Downloading...';
    const a = document.createElement('a');
    a.href = '/api/pipe?url=' + encodeURIComponent(data.url);
    a.download = 'video.mp4';
    a.style.display = 'none';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    label.textContent = 'Download started';
  } catch (e) {
    label.textContent = '\u274C Failed: ' + (e.message || 'Error');
  }
  setTimeout(() => { btn.disabled = false; if (label.textContent.includes('Failed') || label.textContent.includes('Try')) {} else { label.textContent = 'Download'; } }, 5000);
}

document.getElementById('urlInput').addEventListener('keydown', e => { if (e.key === 'Enter') handleDownload(); });
